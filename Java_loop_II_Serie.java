import java.util.Scanner;

public class Java_loop_II_Serie {

	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		
		// Amount of series
		int t = in.nextInt();
		
		// a, b & n for the series
		int a, b, n;
		for (int i = 0; i < t; i++) {
			a = in.nextInt();
			b = in.nextInt();
			n = in.nextInt();
		}
		in.close();
				

	}

}
